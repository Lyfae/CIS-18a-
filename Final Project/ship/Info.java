package ship;

public class Info { 
//initialize the variables
private String name; 
private int member; 
private char choose;
private String address;
public int number;

//create the functions

public String getName() { return name; }
public void setName(String n) { name = n; } 
public int getMember() { return member; }
public void setMember(int m) { member = m; }
public char getChoose() { return choose; } 
public void setChoose(char c) { choose = c; }
public int getNumItems() { return number; } 
public void setNumItems(int n) { number = n; }
public String getAddress() { return address; }
public void setAddress(String a) { address = a; }
} 